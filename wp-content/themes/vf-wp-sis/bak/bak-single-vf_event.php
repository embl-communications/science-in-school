                            <h2>Financial Assistance</h2>


                            <p><strong>Registration Fee Waivers</strong></p>


                            <p>All academic and student registrants are invited to apply for a registration fee waiver,
                                provided by the EMBL Advanced Training Centre Corporate Partnership Programme and EMBO.
                                The registration fee waiver covers the registration sum that you have paid to attend the
                                meeting. Conference participants are not required to pre-pay the registration fee to be
                                selected for a fee waiver for a virtual meeting. If you have already paid the
                                registration fee and are awarded a fee waiver, it will be reimbursed after the meeting.
                                Course participants are required to pay the course fee in advance, which will then be
                                reimbursed after the recipient has attended the course.</p>

                            <h3><strong>Childcare Grants</strong></h3>

                            <p>For participants and speakers with childcare responsibilities there is the possibility to
                                apply for a grant,&nbsp;provided by the EMBL Advanced Training Centre Corporate
                                Partnership Programme and EMBO, to offset childcare costs incurred when participating at
                                a virtual event. Eligible costs include fees for a babysitter or childcare facility or
                                travel costs for a care giver. Please note that priority will be given to early stage
                                researchers. Costs will be reimbursed after the meeting only once a reimbursement form
                                and original receipts have been received. Attendance at the event is required in order
                                to be eligible to receive the reimbursement. In order to apply for this grant, you must
                                be registered by the abstract submission deadline.</p>


                            <h3><strong>Boehringer Ingelheim Fonds Travel Grant</strong></h3>


                            <p>Applies to selected courses only. Availability will be indicated during the abstract or
                                motivation letter submission process.</p>


                            <p>This grant covers costs related to your attendance to the course (registration, travel
                                and accommodation costs). The grant is restricted to PhD students and postdocs who
                                conduct basic biomedical research.<br>Whether you are eligible to apply for a travel
                                grant,&nbsp;depends on when you received your university entrance qualification (e.g.
                                Abitur, A-Levels, High School Diploma, Final State Examination):</p>


                            <p>– for PhD and MD students, as well as graduates, the university entrance qualification
                                must not have been obtained more than 11 years ago at the time of the envisaged
                                course<br>– for postdocs, the university entrance qualification must not have been
                                obtained more than 13 years ago at the time of the envisaged course</p>


                            <h3><strong>Application</strong></h3>


                            <p>Applications for financial assistance can be submitted via the submission portal* (for
                                the submission of abstracts for conferences or the submission of motivation letters for
                                courses) by completing the Financial Assistance Application Section (underneath the
                                section for entering abstract/motivation letter information). The link to the portal can
                                be found in the registration confirmation email that you will receive after registering
                                for the conference or course.</p>


                            <p>For conferences, if you are not submitting an abstract, you can still apply for financial
                                assistance in the submission portal by following the instructions&nbsp;<a
                                        href="https://www.embl.de/training/events/info_participants/fellowships/FA_instructions.pdf"
                                        target="_blank" rel="noreferrer noopener">here</a>. Note that priority will be
                                given to those submitting an abstract to present at the conference. In your application
                                you will be asked to answer questions regarding your motivation for applying, and, for
                                registration fee waivers, the reasons why your lab cannot fund your attendance and how
                                your attendance will make a difference to your career. Application for financial support
                                will not affect the outcome of your registration application.</p>


                            <p>*For some events, applications for Childcare Grants will still be done by email.
                                Information about the grant will be sent out shortly after the abstract/motivation
                                letter deadline. Please contact the event Conference Officer if you have any
                                questions.</p>


                            <h3><strong>Selection</strong></h3>


                            <p>The scientific organisers will select the recipients of registration fee waivers during
                                the abstract selection process for conferences and the participant selection process for
                                courses.&nbsp;Results will be announced approximately 3 – 4 weeks before the event start
                                date.&nbsp;Selection results do not impact your admission to the meeting. Registration
                                fee waiver selection is based on your current work or study location, your motivation
                                for applying, the reasons for needing financial support and the impact this event will
                                have on your career. Childcare grants are allocated based on career stage, with priority
                                given to early stage researchers.</p>


