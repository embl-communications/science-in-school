                        <p></p>


                        <p><strong>EMBL is committed to sharing research advances and sustaining scientific interaction
                                throughout the coronavirus pandemic. We are delighted to announce that the course is
                                going
                                virtual and invite you to join us online.</strong></p>

                        <p>Regulation of translation is a key layer in the control of gene expression, playing a
                            critical role in many biological and pathophysiological settings. In the last decade,
                            methodological advances have provided comprehensive insights into cellular transcriptomes
                            and proteomes, however, the systems-wide analysis of translation has remained an unsolved
                            experimental challenge. The recent advent of ‘ribosome-profiling’ has provided the
                            methodological basis for comprehensive transcriptome-wide analyses of translation at
                            sub-codon resolution by the precise and high-throughput determination of ribosomal positions
                            on mRNA. Its application in a variety of organisms yielded novel insights into protein
                            synthesis and its dynamic regulation, significantly advancing our understanding of the
                            post-transcriptional regulation of gene expression. This practical course will combine
                            theoretical sessions with hands-on experimentation and computation emphasising the key steps
                            of the protocol and computational quality control.</p>

